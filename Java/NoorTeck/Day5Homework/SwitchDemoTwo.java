public class SwitchDemoTwo {

    public static void main(String[] args) {

        int favNumber = 3;

        switch (favNumber) {
            case 3:
            System.out.println("Wednsdey");
            break;
            case 4:
            System.out.println("Thursday");
            break;
            default:
                System.out.println("Not a day of the week ");
        }

    }
}